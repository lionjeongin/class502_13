package exam01;

public class Ex01 { // 자료형 기본값
    static int num1; // 기본값 : 0 -> 변수만 선언해도 기본값이 들어간다!
    static double num2; // 기본값 : 0.0
    static boolean bool1; // 기본값 : false
    static String str; // 기본값 : null -> 값이 없는 상태를 의미하는 null 값이 들어간다.

    public static void main(String[] args) {
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(bool1);
        System.out.println(str);
    }
}
